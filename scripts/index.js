


const hamburger1 = document.querySelector(".hamburger");
function hamburger() {
    hamburger1.classList.toggle("is-active");
    document.querySelector(".navigationMain").classList.toggle("navActive");
    document.querySelector(".hamburgerMenu").classList.toggle("hamburgerActive");
    document.querySelector(".m-img").classList.toggle("m-imgBlur");
    document.body.classList.toggle("overflowNone");
}












